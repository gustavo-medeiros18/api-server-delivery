def comprimento_sem_tipagem(palavra):
    comprimento = len(palavra)
    return comprimento

entrada = "CDC"
resultado_1 = comprimento_sem_tipagem(entrada)
print(f"Comprimento da palavra: {resultado_1}")

entrada = 10
resultado_2 = comprimento_sem_tipagem(entrada)
print(f"Comprimento da palavra: {resultado_2}")