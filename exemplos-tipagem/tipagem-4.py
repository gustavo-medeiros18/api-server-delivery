def dobro_e_comprimento_com_tipagem(numero: int, palavra: str):
    dobro_numero = numero * 2
    comprimento_palavra = len(palavra)

    print(f"Dobro do número: {dobro_numero}")
    print(f"Comprimento da palavra: {comprimento_palavra}")

dobro_e_comprimento_com_tipagem(10, "dev")
dobro_e_comprimento_com_tipagem(20, 30.54)